    class Instrucoes
    {
        static void Main(string[] arg)
        {
            //Ações de um programa são expressas usando instruções
            /*
            {
                Um bloco permite que várias instruções sejam escritas
                em contextos.
            }
            */

            /*
            No C# quando falamos em instruções estamos falando de
            declarações de variáveis e constantes locais
            */

            //Instruções condicionais:
            if()
            switch(){}

            //Instruções de repetição:
            while(){}
            do{}
            for()
            foreach()

            //Instruções auxiliares
            break
            continue
            return

            //Instruções para tratar exceptions
            throw
            try, catch, finally

            //Instruções de using 
            //(dentro de uma classe importarmos referências a pacotes e namespaces)
            using









        }
    }
